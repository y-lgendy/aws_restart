#List

myFruitList=["banana", "cherry", "strawberry"]
print(myFruitList)
print(type(myFruitList))

print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])

myFruitList[2]="orange"
print(myFruitList)


#Tuple

myFinalTuple=("banana", "cherry", "strawberry")
print(myFinalTuple)
print(type(myFinalTuple))

print(myFinalTuple[2])


